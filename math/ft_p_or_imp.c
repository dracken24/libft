#include "../libft.h"

int ft_p_or_imp(int nbr)
{
    if (nbr % 10 == 0 || nbr % 10 == 2 || nbr % 10 == 4
            || nbr % 10 == 6 || nbr % 10 == 8)
        return (true);
    else
        return (false);
    return (-1);
}